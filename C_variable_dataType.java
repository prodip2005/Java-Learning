//we can use underscore(_), number,letters and also dollar sign ($) in java. but $ is not recommended

/*
  variable decleration syntax:
  data_type variable_name;
*/
public class C_variable_dataType {
  public static void main(String[] args)
  {

    boolean a= false;
    int b= 1627252;
    short c= 32677;
    float d=10.12f;    // float er khettre f obossoinuse korte hobe
    double e=10.2;
    System.out.println("A= "+a);
    System.out.println("B= "+b);
    System.out.println("C= "+c);
    System.out.println("D= "+d);
    System.out.println("E= "+e);
  }
}
